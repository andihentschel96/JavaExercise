package MultiplyFloats;

public class MultiplyFloats {
    public static void main(String[] args) {
        float firstNumber = 12.5f;
        float secondNumber = 2.5f;
        float product = firstNumber * secondNumber;

        System.out.println("When you multiply " + firstNumber + " and " + secondNumber +
                " you'll get " + product);
    }
}