package OOP1.LibraryMethods;

public class StandardLibrary {
    public static void main(String[] args) {
        //Standard Library Methods like System.out.print() are built in Methods that are free to use
        //You can call a Method inside the list of parameters of another class
        //Like this:
        System.out.println("The square root of 24 is: " + Math.sqrt(24));
    }
}