package Basics.QuotientRemainder;

public class QuotientRemainder {
    public static void main(String[] args) {
        int a = 25;
        int b = 4;
        float quotient = a / b;
        int remainder = a % b;

        //output
        System.out.println("Quotient: " + quotient);
        System.out.println("Remainder: " + remainder);
    }
}